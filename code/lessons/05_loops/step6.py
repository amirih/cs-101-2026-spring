# while loop
if __name__ == '__main__':
    loop_counter = 0
    while loop_counter < 5:
        print(f"loop_counter: {loop_counter}")
        loop_counter += 1

    print("---------------------------------")
    loop_counter = 10
    while loop_counter < 15:
        print(f"loop_counter: {loop_counter}")
        loop_counter += 1

    print("---------------------------------")
    loop_counter = 20
    while loop_counter < 30:
        print(f"loop_counter: {loop_counter}")
        loop_counter += 2

    print("---------------------------------")
    loop_counter = 30
    while loop_counter > 20:
        print(f"loop_counter: {loop_counter}")
        loop_counter -= 2