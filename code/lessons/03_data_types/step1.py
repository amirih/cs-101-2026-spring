# most common data types in python are: integer, float, string, boolean, list, tuple, dictionary

if __name__ == '__main__':

    # Integer
    age = 25

    # Float
    pi = 3.14

    # String
    name = "Alice"

    # Boolean
    is_student = True

    # List
    fruits = ["apple", "banana", "cherry"]

    # Tuple
    coordinates = (10, 20)

    # Dictionary
    person = {"name": "Alice", "age": 25}

    # Set
    set_numbers = {1, 2, 3}

    print(f"Example of integer: {age}")
    print(f"Example of float: {pi}")
    print(f"Example of string: {name}")
    print(f"Example of boolean: {is_student}")
    print(f"Example of list: {fruits}")
    print(f"Example of tuple: {coordinates}")
    print(f"Example of dictionary: {person}")
    print(f"Example of set: {set_numbers}")
    

    
    

