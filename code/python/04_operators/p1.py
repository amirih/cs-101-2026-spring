# Arithmetic operators

if __name__ == '__main__':
    addition = 10 + 5
    print(f"10 + 5 = {addition}")
    subtraction = 10 - 5
    print(f"10 - 5 = {subtraction}")
    multiplication = 10 * 5
    print(f"10 * 5 = {multiplication}")
    division = 10 / 5
    print(f"10 / 5 = {division}")
    modulus = 10 % 3
    print(f"10 % 3 = {modulus}")
